# 布尔类型的定义
bool1 = True
bool2 = False
print(f"bool1变量的内容是：{bool1},类型是：{type(bool1)}")
print(f"bool2变量的内容是：{bool2},类型是：{type(bool2)}")

# 比较运算符的使用
# == ！= < > <= >=
result = 10>23
print(f"10>23的结果是{result}，结果类型是{type(result)}")
# 字符串也可以比较
name1 = 'fell2'
name2 = 'fell'
print(f"name1==name2的结果是：{name1==name2},类型是：{type(name1==name2)}")
print(f"nam e1>name2的结果是：{name1>name2},类型是：{type(name1>name2)}")